# ARITHMETIC OPERATIONS

# addition
print(2 + 2)

# subtraction 
print(2 - 2)

# multiplication
print(2 * 2)

# floating point division
print(10/3)

# integer division
print(10//3)

# modulus operator
print(10%5)

# exponents
print(2**3)

# incrementation
i = 0
i+=1
print(i)

# decrementation
j = 10
j-=3
print(j)

